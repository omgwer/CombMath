/**
ะคัะฝะบัะธั dfs ัะตะฐะปะธะทัะตั ะฐะปะณะพัะธัะผ ะณะปัะฑะธะฝะฝะพะณะพ ะฟะพะธัะบะฐ (Depth-First Search). ะะฝ ะธัะฟะพะปัะทัะตััั ะดะปั ะฟะพะธัะบะฐ ะผะฐะบัะธะผะฐะปัะฝะพะณะพ ะฟะฐัะพัะพัะตัะฐะฝะธั ะฒ ะดะฒัะดะพะปัะฝะพะผ ะณัะฐัะต.
 * @param v - ัะตะบััะฐั ะฒะตััะธะฝะฐ, ั ะบะพัะพัะพะน ะฝะฐัะธะฝะฐะตััั ะพะฑัะพะด
 * @param visited - ะฒะตะบัะพั ะฑัะปะตะฒัั ะทะฝะฐัะตะฝะธะน, ะพััะฐะถะฐััะธะน ะฟะพัะตัะตะฝะธะต ะฒะตััะธะฝ ะณัะฐัะฐ
 * @param match - ะฒะตะบัะพั, ัะพะดะตัะถะฐัะธะน ัะตะบััะตะต ะฟะฐัะพัะพัะตัะฐะฝะธะต (ะธะฝะดะตะบัั ะฒะตััะธะฝ, ัะฒัะทะฐะฝะฝัั ัะตะฑัะพะผ)
 * @param graph - ะดะฒัะผะตัะฝัะน ะฒะตะบัะพั, ะฟัะตะดััะฐะฒะปัััะธะน ะณัะฐั
 */
bool dfs(int v, vector<bool>& visited, vector<int>& match, const vector<vector<int>>& graph)
{
    // ะัะปะธ ะฒะตััะธะฝะฐ v ัะถะต ะฑัะปะฐ ะฟะพัะตัะตะฝะฐ (visited[v] ัะฐะฒะฝะพ true), ะฒะพะทะฒัะฐัะฐะตััั false.
    if (visited[v])
    {
        return false;
    }
    //ะะพะผะตัะฐะตะผ ะฒะตััะธะฝั v ะบะฐะบ ะฟะพัะตัะตะฝะฝัั (visited[v] = true).
    visited[v] = true;

    for (size_t i = 0; i < graph[v].size(); i++)
    {
        int to = graph[v][i];
        // ะัะปะธ ะฒะตััะธะฝะฐ to ะตัะต ะฝะต ะธะผะตะตั ะฟะฐัั (match[to] == -1) ะธะปะธ ะผะพะถะฝะพ ะฝะฐะนัะธ ัะฒะตะปะธัะธะฒะฐัััั ัะตะฟั ะธะท ัะตะบััะตะน ะฟะฐัั match[to], ะฒัะทัะฒะฐะตััั ัะตะบัััะธะฒะฝะพ dfs ะดะปั ะฒะตััะธะฝั match[to].
        if (match[to] == -1 || dfs(match[to], visited, match, graph))
        {
            //ะัะปะธ ัะตะบัััะธะฒะฝัะน ะฒัะทะพะฒ dfs ะฒะพะทะฒัะฐัะฐะตั true, ัะพ ัะตะบััะฐั ะฒะตััะธะฝะฐ v ะธ ะฒะตััะธะฝะฐ to ะพะฑัะฐะทััั ะฟะฐัั, ะธ ะฟัะพะธััะพะดะธั ะพะฑะฝะพะฒะปะตะฝะธะต ะฟะฐัะพัะพัะตัะฐะฝะธั: match[to] = v, ะธ ััะฝะบัะธั ะฒะพะทะฒัะฐัะฐะตั true.
            match[to] = v;
            return true;
        }
    }
    //ะัะปะธ ะฝะธ ะพะดะฝะพ ะธะท ัะตะฑะตั ะธะท ัะตะบััะตะน ะฒะตััะธะฝั v ะฝะต ะฟัะธะฒะพะดะธั ะบ ัะฒะตะปะธัะธะฒะฐััะตะน ัะตะฟะธ, ััะฝะบัะธั ะฒะพะทะฒัะฐัะฐะตั false.
    return false;
}

void ParseTextFile(ifstream& in, vector<vector<int>>& graph, size_t size)
{
    for (int i = 0; i < size; i++)
    {
        for (int j = 0; j < size; j++)
        {
            in >> graph[i][j];
        }
    }
    in.close();
}

/**
 *  ะคัะฝะบัะธั FindCompleteMatching() ะธัะฟะพะปัะทัะตััั ะดะปั ะฟะพะธัะบะฐ ะผะฐะบัะธะผะฐะปัะฝะพะณะพ ะฟะฐัะพัะพัะตัะฐะฝะธั ะฒ ะดะฒัะดะพะปัะฝะพะผ ะณัะฐัะต.
 * @param arr - ะดะฒัะดะพะปัะฝัะน ะณัะฐั ะฒ ะฒะธะดะต ะผะฐััะธัั ัะผะตะถะฝะพััะธ
 * @param size - ัะฐะทะผะตั ะณัะฐัะฐ
 */
vector<int> FindCompleteMatching(vector<vector<int>>& arr, int size)
{
    //ะกะพะทะดะฐะตััั ะฟัััะพะน ะดะฒัะผะตัะฝัะน ะฒะตะบัะพั ribsList, ะบะพัะพััะน ะฑัะดะตั ัะพะดะตัะถะฐัั ัะฟะธัะบะธ ัะผะตะถะฝะพััะธ ะดะปั ะบะฐะถะดะพะน ะฒะตััะธะฝั ะณัะฐัะฐ. ะญัะพั ะฒะตะบัะพั ะฑัะดะตั ะฟัะตะดััะฐะฒะปััั ะณัะฐั ะฒ ะฒะธะดะต ัะฟะธัะบะพะฒ ัะผะตะถะฝะพััะธ.
    vector<vector<int>> ribsList(size);
    for (int i = 0; i < size; ++i)
    {
        for (int j = 0; j < size; ++j)
        {
            // ะะปั ะบะฐะถะดะพะน ะฒะตััะธะฝั i ะธ ะบะฐะถะดะพะน ะฒะตััะธะฝั j ะฟัะพะฒะตััะตััั, ะตััั ะปะธ ะผะตะถะดั ะฝะธะผะธ ัะตะฑัะพ ะฒ ะธััะพะดะฝะพะผ ะณัะฐัะต (arr[i][j] == 1).
            if (arr[i][j] == 1)
            {
                //ะัะปะธ ัะตะฑัะพ ะตััั, ัะพ ะดะพะฑะฐะฒะปัะตััั ัะตะฑัะพ ะธะท ะฒะตััะธะฝั i ะฒ ะฒะตััะธะฝั j ะฒ ัะฟะธัะพะบ ัะผะตะถะฝะพััะธ.
                ribsList[i].push_back(j);
            }
        }
    }

    // ะกะพะทะดะฐะตััั ะฒะตะบัะพั matchingList ัะฐะทะผะตัะพะผ size, ะธ ะบะฐะถะดัะน ะตะณะพ ัะปะตะผะตะฝั ะธะฝะธัะธะฐะปะธะทะธััะตััั ะทะฝะฐัะตะฝะธะตะผ -1. ะญัะพั ะฒะตะบัะพั ะฑัะดะตั ััะฐะฝะธัั ัะตะบััะตะต ะฟะฐัะพัะพัะตัะฐะฝะธะต.
    vector<int> matchingList(size, -1);
    // ะกะพะทะดะฐะตััั ะฒะตะบัะพั visited ัะฐะทะผะตัะพะผ size ะธ ะทะฐะฟะพะปะฝัะตััั ะทะฝะฐัะตะฝะธัะผะธ false.
    vector<bool> visited(size, false);

    for (int v = 0; v < size; ++v)
    {
        //oัะธัะฐะตะผ Used
        visited.assign(size, false);
        //ะัะทัะฒะฐะตััั ััะฝะบัะธั dfs, ะบะพัะพัะฐั ัะตะฐะปะธะทัะตั ะณะปัะฑะธะฝะฝัะน ะฟะพะธัะบ ะดะปั ะฟะพะธัะบะฐ ัะฒะตะปะธัะธะฒะฐััะธั ัะตะฟะตะน ะธ ะพะฑะฝะพะฒะปะตะฝะธั ะฟะฐัะพัะพัะตัะฐะฝะธั.
        dfs(v, visited, matchingList, ribsList);
    }

    //ะะพะทะฒัะฐัะฐะตััั ะฒะตะบัะพั matchingList, ัะพะดะตัะถะฐัะธะน ะผะฐะบัะธะผะฐะปัะฝะพะต ะฟะฐัะพัะพัะตัะฐะฝะธะต.
    return matchingList;
}

int main()
{
    string fileName = "test.txt";
    ifstream input(fileName);
    if (!input.is_open())
    {
        cout << "File not found";
        return 1;
    }
    int matrixSize, rowSize;
    input >> matrixSize >> rowSize;

    vector<vector<int>> graph(matrixSize, vector<int>(matrixSize, 0));
    ParseTextFile(input, graph, matrixSize);

    vector<int> match = FindCompleteMatching(graph, matrixSize);

    if (find(match.begin(), match.end(), -1) != match.end())
    {
        cout << "No" << endl;
    }
    else
    {
        cout << "Yes" << endl;
        for (int i = 0; i < graph.size(); ++i)
        {
            cout << match[i] + 1 << ", " << i + 1 << endl;
        }
    }

    return 0;
}