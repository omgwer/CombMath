/*
–§—É–Ω–∫—Ü–∏—è dfs —Ä–µ–∞–ª–∏–∑—É–µ—Ç –∞–ª–≥–æ—Ä–∏—Ç–º –≥–ª—É–±–∏–Ω–Ω–æ–≥–æ –ø–æ–∏—Å–∫–∞ (Depth-First Search). –û–Ω –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ –¥–∞–Ω–Ω–æ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç–µ –¥–ª—è –ø–æ–∏—Å–∫–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏—è –≤ –¥–≤—É–¥–æ–ª—å–Ω–æ–º –≥—Ä–∞—Ñ–µ.

–ê–ª–≥–æ—Ä–∏—Ç–º —Ä–∞–±–æ—Ç–∞–µ—Ç —Å–ª–µ–¥—É—é—â–∏–º –æ–±—Ä–∞–∑–æ–º:
–ï—Å–ª–∏ –≤–µ—Ä—à–∏–Ω–∞ v —É–∂–µ –±—ã–ª–∞ –ø–æ—Å–µ—â–µ–Ω–∞ (visited[v] —Ä–∞–≤–Ω–æ true), –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç—Å—è false.
–ü–æ–º–µ—á–∞–µ–º –≤–µ—Ä—à–∏–Ω—É v –∫–∞–∫ –ø–æ—Å–µ—â–µ–Ω–Ω—É—é (visited[v] = true).
–î–ª—è –∫–∞–∂–¥–æ–π —Å–º–µ–∂–Ω–æ–π –≤–µ—Ä—à–∏–Ω—ã to —Å —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—à–∏–Ω–æ–π v:
–ï—Å–ª–∏ –≤–µ—Ä—à–∏–Ω–∞ to –µ—â–µ –Ω–µ –∏–º–µ–µ—Ç –ø–∞—Ä—ã (match[to] == -1) –∏–ª–∏ –º–æ–∂–Ω–æ –Ω–∞–π—Ç–∏ —É–≤–µ–ª–∏—á–∏–≤–∞—é—â—É—é —Ü–µ–ø—å –∏–∑ —Ç–µ–∫—É—â–µ–π –ø–∞—Ä—ã match[to], –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ dfs –¥–ª—è –≤–µ—Ä—à–∏–Ω—ã match[to].
–ï—Å–ª–∏ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—ã–π –≤—ã–∑–æ–≤ dfs –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç true, —Ç–æ —Ç–µ–∫—É—â–∞—è –≤–µ—Ä—à–∏–Ω–∞ v –∏ –≤–µ—Ä—à–∏–Ω–∞ to –æ–±—Ä–∞–∑—É—é—Ç –ø–∞—Ä—É, –∏ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏—è: match[to] = v, –∏ —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç true.
–ï—Å–ª–∏ –Ω–∏ –æ–¥–Ω–æ –∏–∑ —Ä–µ–±–µ—Ä –∏–∑ —Ç–µ–∫—É—â–µ–π –≤–µ—Ä—à–∏–Ω—ã v –Ω–µ –ø—Ä–∏–≤–æ–¥–∏—Ç –∫ —É–≤–µ–ª–∏—á–∏–≤–∞—é—â–µ–π —Ü–µ–ø–∏, —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç false.
*/
/**
 * @param v - —Ç–µ–∫—É—â–∞—è –≤–µ—Ä—à–∏–Ω–∞, —Å –∫–æ—Ç–æ—Ä–æ–π –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –æ–±—Ö–æ–¥
 * @param visited - –≤–µ–∫—Ç–æ—Ä –±—É–ª–µ–≤—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π, –æ—Ç—Ä–∞–∂–∞—é—â–∏–π –ø–æ—Å–µ—â–µ–Ω–∏–µ –≤–µ—Ä—à–∏–Ω –≥—Ä–∞—Ñ–∞
 * @param match - –≤–µ–∫—Ç–æ—Ä, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π —Ç–µ–∫—É—â–µ–µ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ (–∏–Ω–¥–µ–∫—Å—ã –≤–µ—Ä—à–∏–Ω, —Å–≤—è–∑–∞–Ω–Ω—ã—Ö —Ä–µ–±—Ä–æ–º)
 * @param graph - –¥–≤—É–º–µ—Ä–Ω—ã–π –≤–µ–∫—Ç–æ—Ä, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∏–π –≥—Ä–∞—Ñ
 */
bool dfs(int v, vector<bool>& visited, vector<int>& match, const vector<vector<int>>& graph)
{
    if (visited[v])
    {
        return false;
    }
    visited[v] = true;

    for (size_t i = 0; i < graph[v].size(); i++)
    {
        int to = graph[v][i];
        if (match[to] == -1 || dfs(match[to], visited, match, graph))
        {
            match[to] = v;
            return true;
        }
    }
    return false;
}

void ParseTextFile(ifstream& in, vector<vector<int>>& graph, size_t size)
{
    for (int i = 0; i < size; i++)
    {
        for (int j = 0; j < size; j++)
        {
            in >> graph[i][j];
        }
    }
    in.close();
}

/**
 *  –§—É–Ω–∫—Ü–∏—è FindCompleteMatching() –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø–æ–∏—Å–∫–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏—è –≤ –¥–≤—É–¥–æ–ª—å–Ω–æ–º –≥—Ä–∞—Ñ–µ.
 * @param arr - –¥–≤—É–¥–æ–ª—å–Ω—ã–π –≥—Ä–∞—Ñ –≤ –≤–∏–¥–µ –º–∞—Ç—Ä–∏—Ü—ã —Å–º–µ–∂–Ω–æ—Å—Ç–∏
 * @param size - —Ä–∞–∑–º–µ—Ä –≥—Ä–∞—Ñ–∞
 */
vector<int> FindCompleteMatching(vector<vector<int>>& arr, int size)
{
    //–°–æ–∑–¥–∞–µ—Ç—Å—è –ø—É—Å—Ç–æ–π –¥–≤—É–º–µ—Ä–Ω—ã–π –≤–µ–∫—Ç–æ—Ä ribsList, –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ—Ç —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Å–ø–∏—Å–∫–∏ —Å–º–µ–∂–Ω–æ—Å—Ç–∏ –¥–ª—è –∫–∞–∂–¥–æ–π –≤–µ—Ä—à–∏–Ω—ã –≥—Ä–∞—Ñ–∞. –≠—Ç–æ—Ç –≤–µ–∫—Ç–æ—Ä –±—É–¥–µ—Ç –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—Ç—å –≥—Ä–∞—Ñ –≤ –≤–∏–¥–µ —Å–ø–∏—Å–∫–æ–≤ —Å–º–µ–∂–Ω–æ—Å—Ç–∏.
    vector<vector<int>> ribsList(size);
    for (int i = 0; i < size; ++i)
    {
        for (int j = 0; j < size; ++j)
        {
            // –î–ª—è –∫–∞–∂–¥–æ–π –≤–µ—Ä—à–∏–Ω—ã i –∏ –∫–∞–∂–¥–æ–π –≤–µ—Ä—à–∏–Ω—ã j –ø—Ä–æ–≤–µ—Ä—è–µ—Ç—Å—è, –µ—Å—Ç—å –ª–∏ –º–µ–∂–¥—É –Ω–∏–º–∏ —Ä–µ–±—Ä–æ –≤ –∏—Å—Ö–æ–¥–Ω–æ–º –≥—Ä–∞—Ñ–µ (arr[i][j] == 1).
            if (arr[i][j] == 1)
            {
                //–ï—Å–ª–∏ —Ä–µ–±—Ä–æ –µ—Å—Ç—å, —Ç–æ –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è —Ä–µ–±—Ä–æ –∏–∑ –≤–µ—Ä—à–∏–Ω—ã i –≤ –≤–µ—Ä—à–∏–Ω—É j –≤ —Å–ø–∏—Å–æ–∫ —Å–º–µ–∂–Ω–æ—Å—Ç–∏.
                ribsList[i].push_back(j);
            }
        }
    }

    // –°–æ–∑–¥–∞–µ—Ç—Å—è –≤–µ–∫—Ç–æ—Ä matchingList —Ä–∞–∑–º–µ—Ä–æ–º size, –∏ –∫–∞–∂–¥—ã–π –µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏–µ–º -1. –≠—Ç–æ—Ç –≤–µ–∫—Ç–æ—Ä –±—É–¥–µ—Ç —Ö—Ä–∞–Ω–∏—Ç—å —Ç–µ–∫—É—â–µ–µ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ.
    vector<int> matchingList(size, -1);
    // –°–æ–∑–¥–∞–µ—Ç—Å—è –≤–µ–∫—Ç–æ—Ä visited —Ä–∞–∑–º–µ—Ä–æ–º size –∏ –∑–∞–ø–æ–ª–Ω—è–µ—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏—è–º–∏ false.
    vector<bool> visited(size, false);

    for (int v = 0; v < size; ++v)
    {
        //o—á–∏—â–∞–µ–º Used
        visited.assign(size, false);
        //–í—ã–∑—ã–≤–∞–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è dfs(v, visited, matchingList, ribsList), –∫–æ—Ç–æ—Ä–∞—è —Ä–µ–∞–ª–∏–∑—É–µ—Ç –≥–ª—É–±–∏–Ω–Ω—ã–π –ø–æ–∏—Å–∫ –¥–ª—è –ø–æ–∏—Å–∫–∞ —É–≤–µ–ª–∏—á–∏–≤–∞—é—â–∏—Ö —Ü–µ–ø–µ–π –∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏—è.
        dfs(v, visited, matchingList, ribsList);
    }

    //–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç—Å—è –≤–µ–∫—Ç–æ—Ä matchingList, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –ø–∞—Ä–æ—Å–æ—á–µ—Ç–∞–Ω–∏–µ.
    return matchingList;
}

int main()
{
    string fileName = "test.txt";
    ifstream input(fileName);
    if (!input.is_open())
    {
        cout << "File not found";
        return 1;
    }
    int matrixSize, rowSize;
    input >> matrixSize >> rowSize;

    vector<vector<int>> graph(matrixSize, vector<int>(matrixSize, 0));
    ParseTextFile(input, graph, matrixSize);

    vector<int> match = FindCompleteMatching(graph, matrixSize);

    if (find(match.begin(), match.end(), -1) != match.end())
    {
        cout << "No" << endl;
    }
    else
    {
        cout << "Yes" << endl;
        for (int i = 0; i < graph.size(); ++i)
        {
            cout << match[i] + 1 << ", " << i + 1 << endl;
        }
    }

    return 0;
}